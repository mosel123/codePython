#Operadores de asignacion
import os
#Valor inicial
a = 5
#Suma en asignacion
a += 5
print(a)
#Resta en asignacion
a -= 5
print(a)
#Producto en asignacion
a *= 5
print(a)
#Division en asignacion
a /= 5
print(a)
#Potencia en asignacion
a **= 5
print(a)
#Modulo en asignacion
a %= 5
print(a)

