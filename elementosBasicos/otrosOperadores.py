import os
num1 = 10
num2 = 3

#resultado sin decimales
resultado = num1 // num2
print(resultado)

#resultado del residuo
resultado = num1 % num2
print(resultado)

#resultado el exponente
resultado = num1 ** num2
print(resultado)

#Uso de parentesis
resultado = 3**3 * (13/5 - (2*4)) #El resultado es -145.8
print(resultado)

