#operadores relacionales
import os

num1 = 1
num2 = 2
num3 = 3

#Menor que...
resultado = num1 < num2
print(resultado)

#Mayor que...
resultado = num1 > num2
print(resultado)

#Igual que...
resultado = num1 + num2 == num3
print(resultado)

#Diferente que...
resultado = num1 + num3 != num2
print(resultado)

#Menor-igual que...
resultado = num1 + num3 <= num2 
print(resultado)

#Mayor-igual que...
resultado = num1 + num3 >= num2
print(resultado)

os.system("PAUSE")