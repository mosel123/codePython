#Funciones integradas
import os
#Convertir cadena a entero
num = int("10")
print(num)
#Convertir entero a cadena
cadena =  str(10.50)
print(cadena)
#Convertir entero a binario
binario = bin(100)
print(binario)
#Convertir a hexadecimal
hexadecimal = hex(100)
print(hexadecimal)
#Convertir base a decimal
entero = int("0xFF",16)
print(entero)
#Obtener valor absoluto
absoluto = abs(-10)
print(absoluto)
#Redondear un numero
redondear = round(5.6)
print(redondear)
#Contador de caracteres
caracteres = len("Abecedario")
print(caracteres)

